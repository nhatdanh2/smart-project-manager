export const dicts: Record<Lang, typeof vi> = { vi, en };

export type Dict = typeof vi;

export function t(lang: Lang, path: string, ...args: any[]): string {
  const dict = dicts[lang] ?? vi;
  const parts = path.split(".");
  let cur: any = dict;
  for (const p of parts) {
    if (cur && typeof cur === "object" && p in cur) {
      cur = cur[p];
    } else {
      return path;
    }
  }
  if (typeof cur === "function") {
    try {
      return String(cur(...args));
    } catch {
      return path;
    }
  }
  return typeof cur === "string" ? cur : path;
}
